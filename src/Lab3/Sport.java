package Lab3;
/* Sport is a persistent class representing a database entity table.
*/
public class Sport {
	private int ID;
	private String Sport;
	
	public int getID() {
		return ID;
	}
	public void setID(int id) {
		ID = id;	
	}
	public String getSport() {
		return Sport;
	}
	public void setSport(String sport) {
		Sport = sport;	
	}
	
}
